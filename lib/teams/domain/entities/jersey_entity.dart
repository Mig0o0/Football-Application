class JerseyEntity {
  final String type;
  final String base;
  final String number;
  final bool squares;
  final bool stripes;
  final bool horizontalStripes;
  final bool split;
  final String shirtType;
  final String sleeveDetail;

  JerseyEntity({
    required this.type,
    required this.base,
    required this.number,
    required this.squares,
    required this.stripes,
    required this.horizontalStripes,
    required this.split,
    required this.shirtType,
    required this.sleeveDetail,
  });
}
